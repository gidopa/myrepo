
 // + - * / % (mod)
  
 
public class Opr01 {

	public static void main(String[] args) {
		int a = 10, b=4, res;
		res = a + b;
		System.out.println(a + "+" + b + "=" + res);
		res = a - b ;
		System.out.println(a + "-" + b + "=" + res);
		res = a * b ;
		System.out.println(a + "*" + b + "=" + res);
		res = a / b ;
		System.out.println(a + "/" + b + "=" + res);
		res = a % b ;
		System.out.println(a + "%" + b + "=" + res);
		
		
	}

}
