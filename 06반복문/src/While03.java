
public class While03 {

	public static void main(String[] args) {
		int i = 0;

		do {
			i++;
			System.out.print(i);
			
		} while (i < 5);
		
		System.out.println("");
		System.out.println("-----------------------------");

		int j =1;
		do {
			System.out.print(j);
			j++;
		}while(j<6);
	}
	

}
