// example : Java 상수 종류
public class Date01 {

	public static void main(String[] args) {
		 // 정수 : int
		 System.out.println(1);
		 // 실수 : float, double
		 System.out.println(1.6);
		 // 문자 : 문자형 single quotation 
		 System.out.println('a');
		 // 논리형 : boolean
		 System.out.println(true);
		  
		 
		
	}

}
