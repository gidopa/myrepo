// 더 다양한 상수
public class Date02 {

	public static void main(String[] args) {
		// long 형 상수 : 큰 int 숫자뒤에 L, l
		System.out.println(10L);
		// float 자료형 : 실수
		System.out.println(1.5f);
		System.out.println(1.5d);
		// String : double quotation 
		System.out.println("hi, hello");
		System.out.println("d");
		
	}

}
