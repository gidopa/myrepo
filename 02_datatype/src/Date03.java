
public class Date03 {

	//main -> 프로그램을 시작 시키는 기능의 함수
	public static void main(String[] args) {
		//a = 1; 선언이 없음
		/*자바에서 변수메모리에 저장 된 데이터를 꺼내서 사용하기 위해
		 * 변수를 먼저 선언해야함
		 * 자료형을 특정해야함 */
		int var; // 변수 선언
		var=1;   // 변수 초기화
		int var1 = 4; // 선언 초기화 한번에
		System.out.printf("var = %d, var1 = %d\n",var,var1);
		System.out.println(var1);
	}

}
